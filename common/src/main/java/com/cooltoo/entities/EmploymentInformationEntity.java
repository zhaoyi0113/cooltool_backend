package com.cooltoo.entities;

import com.cooltoo.constants.CommonStatus;
import com.cooltoo.constants.EmploymentType;

import javax.persistence.*;
import java.util.Date;

/**
 * Created by hp on 2016/4/20.
 */
@Entity
@Table(name = "nursego_employment_information")
public class EmploymentInformationEntity {
    private long id;
    private Date time;
    private CommonStatus status;
    private String title;
    private long frontCover;
    private String url;
    private int grade;
    private EmploymentType type;

    @Id
    @GeneratedValue
    @Column(name = "id")
    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }

    @Column(name = "title")
    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    @Column(name = "front_cover")
    public long getFrontCover() {
        return frontCover;
    }

    public void setFrontCover(long frontCover) {
        this.frontCover = frontCover;
    }

    @Column(name = "url")
    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    @Column(name = "grade")
    public int getGrade() {
        return grade;
    }

    @Column(name = "type")
    public EmploymentType getType() {
        return type;
    }

    public void setType(EmploymentType type) {
        this.type = type;
    }

    public void setGrade(int grade) {
        this.grade = grade;
    }

    @Column(name = "time_created")
    public Date getTime() {
        return time;
    }

    public void setTime(Date time) {
        this.time = time;
    }

    @Column(name = "status")
    @Enumerated
    public CommonStatus getStatus() {
        return status;
    }

    public void setStatus(CommonStatus status) {
        this.status = status;
    }

    @Override
    public String toString() {
        StringBuilder msg = new StringBuilder();
        msg.append(getClass()).append("@").append(hashCode()).append("[");
        msg.append("id=").append(id);
        msg.append(", grade=").append(grade);
        msg.append(", title=").append(title);
        msg.append(", frontCover=").append(frontCover);
        msg.append(", url").append(url);
        msg.append(", status=").append(status);
        msg.append(", time=").append(time);
        msg.append("]");
        return msg.toString();
    }
}
